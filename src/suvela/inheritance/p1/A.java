package suvela.inheritance.p1;

public class A {
protected int x=10;
public int y=20;
public void m1() {
	System.out.println("A m1 y"+y);
}
protected void m2() {
	System.out.println("A m2 x"+x);
}
public static void m4() {
	System.out.println("A m4");
}
public void m5(int x) {
	System.out.println("A m5");
}
}
